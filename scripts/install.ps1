# cinaauth Installation Script for Windows
# Usage: iwr -useb https://raw.githubusercontent.com/cinagroup/cinaauth/main/scripts/install.ps1 | iex

$ErrorActionPreference = 'Stop'

# Configuration
$REPO = 'cinagroup/cinaauth'
$BINARY_NAME = 'authframework'
$INSTALL_DIR = "${env:ProgramFiles}\cinaauth"
$CONFIG_DIR = "${env:USERPROFILE}\.config\authframework"
$TEMP_DIR = "${env:TEMP}\authframework-install"

# Create temp directory
if (Test-Path $TEMP_DIR) {
    Remove-Item -Path $TEMP_DIR -Recurse -Force
}
New-Item -ItemType Directory -Path $TEMP_DIR | Out-Null

# Helper Functions
function Write-ColorOutput {
    param(
        [string]$Message,
        [string]$Color = 'White',
        [string]$Prefix = ''
    )
    
    if ($Prefix) {
        Write-Host "$Prefix " -NoNewline -ForegroundColor $Color
    }
    Write-Host $Message
}

function Write-Info {
    param([string]$Message)
    Write-ColorOutput -Message $Message -Color Cyan -Prefix 'ℹ'
}

function Write-Success {
    param([string]$Message)
    Write-ColorOutput -Message $Message -Color Green -Prefix '✓'
}

function Write-Error {
    param([string]$Message)
    Write-ColorOutput -Message $Message -Color Red -Prefix '✗'
}

function Write-Warning {
    param([string]$Message)
    Write-ColorOutput -Message $Message -Color Yellow -Prefix '⚠'
}

function Get-LatestVersion {
    try {
        Write-Info 'Fetching latest version...'
        $response = Invoke-RestMethod -Uri "https://api.github.com/repos/$REPO/releases/latest"
        $version = $response.tag_name
        
        if ([string]::IsNullOrEmpty($version)) {
            throw 'Failed to get latest version'
        }
        
        Write-Success "Latest version: $version"
        return $version
    }
    catch {
        Write-Error "Failed to fetch latest version: $_"
        exit 1
    }
}

function Get-Architecture {
    $arch = $env:PROCESSOR_ARCHITECTURE
    
    switch ($arch) {
        'AMD64' { return 'x86_64' }
        'ARM64' { return 'aarch64' }
        default {
            Write-Error "Unsupported architecture: $arch"
            exit 1
        }
    }
}

function Download-And-Install {
    param(
        [string]$Version,
        [string]$Arch
    )
    
    $TARGET = "${Arch}-pc-windows-msvc"
    $ARCHIVE_NAME = "${BINARY_NAME}-${Version}-${TARGET}.zip"
    $DOWNLOAD_URL = "https://github.com/${REPO}/releases/download/${Version}/${ARCHIVE_NAME}"
    
    Write-Info "Downloading cinaauth ${Version} for ${TARGET}..."
    
    try {
        $archivePath = Join-Path $TEMP_DIR $ARCHIVE_NAME
        Invoke-WebRequest -Uri $DOWNLOAD_URL -OutFile $archivePath -UseBasicParsing
        
        Write-Info 'Extracting archive...'
        Expand-Archive -Path $archivePath -DestinationPath $TEMP_DIR -Force
        
        Write-Info "Installing binaries to ${INSTALL_DIR}..."
        
        # Create install directory
        if (-not (Test-Path $INSTALL_DIR)) {
            New-Item -ItemType Directory -Path $INSTALL_DIR -Force | Out-Null
        }
        
        # Copy binaries
        $binaries = @('authframework-server.exe', 'authframework-cli.exe')
        foreach ($binary in $binaries) {
            $sourcePath = Join-Path $TEMP_DIR $binary
            if (Test-Path $sourcePath) {
                Copy-Item -Path $sourcePath -Destination $INSTALL_DIR -Force
                Write-Success "Installed $binary"
            }
        }
        
        # Add to PATH if not already present
        $currentPath = [Environment]::GetEnvironmentVariable('Path', 'Machine')
        if ($currentPath -notlike "*$INSTALL_DIR*") {
            Write-Info "Adding ${INSTALL_DIR} to system PATH..."
            [Environment]::SetEnvironmentVariable(
                'Path',
                "$currentPath;$INSTALL_DIR",
                'Machine'
            )
            Write-Success 'Added to PATH (restart terminal to use)'
        }
        
        Write-Success 'cinaauth installed successfully!'
    }
    catch {
        Write-Error "Installation failed: $_"
        exit 1
    }
}

function Create-SampleConfig {
    Write-Info 'Creating sample configuration...'
    
    if (-not (Test-Path $CONFIG_DIR)) {
        New-Item -ItemType Directory -Path $CONFIG_DIR -Force | Out-Null
    }
    
    $configPath = Join-Path $CONFIG_DIR 'config.toml'
    
    $configContent = @"
# cinaauth Configuration File
# Generated by install script

[server]
host = "127.0.0.1"
port = 8080
workers = 4

[storage]
# Options: "memory", "sqlite", "postgres", "redis"
backend = "sqlite"
connection_string = "${env:LOCALAPPDATA}\authframework\data.db"

[security]
# Generate a secure secret key:
# openssl rand -base64 32
secret_key = "CHANGE_THIS_TO_A_SECURE_RANDOM_KEY"

[jwt]
issuer = "authframework"
access_token_ttl = 3600      # 1 hour
refresh_token_ttl = 2592000  # 30 days

[rate_limiting]
enabled = true
requests_per_second = 100
burst_size = 200

[cors]
enabled = true
allowed_origins = ["http://localhost:3000", "http://localhost:8080"]

[logging]
level = "info"
format = "json"

# OAuth 2.0 Configuration
[oauth]
enable_pkce = true
